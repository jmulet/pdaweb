package org.iesapp.fitxes.forms;

/**
 *
 * @author Josep
 */
public class BeanFitxaCursSimple {

    protected String grup="";
    protected String any_academic="";
    protected String nivell="";
    protected String professor="";
    protected String curs="";
    protected String sancions="";

    /**
     * @return the grup
     */
    public String getGrup() {
        return grup;
    }

    /**
     * @param grup the grup to set
     */
    public void setGrup(String grup) {
        this.grup = grup;
    }

    /**
     * @return the any_academic
     */
    public String getAny_academic() {
        return any_academic;
    }

    /**
     * @param any_academic the any_academic to set
     */
    public void setAny_academic(String any_academic) {
        this.any_academic = any_academic;
    }

    /**
     * @return the nivell
     */
    public String getNivell() {
        return nivell;
    }

    /**
     * @param nivell the nivell to set
     */
    public void setNivell(String nivell) {
        this.nivell = nivell;
    }

    /**
     * @return the professor
     */
    public String getProfessor() {
        return professor;
    }

    /**
     * @param professor the professor to set
     */
    public void setProfessor(String professor) {
        this.professor = professor;
    }

    /**
     * @return the curs
     */
    public String getCurs() {
        return curs;
    }

    /**
     * @param curs the curs to set
     */
    public void setCurs(String curs) {
        this.curs = curs;
    }

    /**
     * @return the sancions
     */
    public String getSancions() {
        return sancions;
    }

    /**
     * @param sancions the sancions to set
     */
    public void setSancions(String sancions) {
        this.sancions = sancions;
    }

}
